export default function GetPosition(id?: number) {
  switch (id) {
    case 1:
      return "Backend Developer";
      break;

    case 2:
      return "Frontend Developer";
      break;

    case 3:
      return "Tester";
      break;

    case 4:
      return "Project Manager";
      break;

    case 5:
      return "Business Analysis";
      break;

    case 6:
      return "Admin";
      break;
  }
}
